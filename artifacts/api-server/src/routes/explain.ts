router.post("/text/explain", async (req, res) => {
  try {
    const parsed = ExplainMcqBody.safeParse(req.body);
    if (!parsed.success) {
      res.status(400).json({ error: "Invalid request body." });
      return;
    }

    const { sections } = parsed.data;

    if (!sections || sections.length === 0) {
      res.status(400).json({ error: "No sections provided." });
      return;
    }

    const apiConfig = getOpenAIClient(req);
    if (!apiConfig) {
      res.status(400).json({ error: "API key not configured. Please add an API key from the API button." });
      return;
    }

    const { client, model } = apiConfig;
    const CONCURRENCY = 3;
    const results: Array<{ title: string; content: string; explanation: string }> = new Array(sections.length);

    for (let i = 0; i < sections.length; i += CONCURRENCY) {
      const batch = sections.slice(i, i + CONCURRENCY);
      const batchResults = await Promise.all(
        batch.map(async (section) => {
          const explanation = await generateExplanationsForSection(section.title, section.content, client, model);
          return { title: section.title, content: section.content, explanation };
        })
      );
      batchResults.forEach((r, batchIdx) => {
        results[i + batchIdx] = r;
      });
    }

    res.json({ sections: results });
  } catch (error: any) {
    console.error("Error generating explanations:", error);

    const status = error?.status ?? 500;
    const apiMessage = error?.error?.message ?? error?.message;
    const code = error?.code ?? error?.error?.code;

    if (status === 401 || code === "invalid_api_key") {
      res.status(401).json({ error: "API key টি সঠিক নয়। API Manager থেকে সঠিক key দিন।" });
    } else if (status === 429) {
      res.status(429).json({ error: "API rate limit শেষ হয়ে গেছে। কিছুক্ষণ পর আবার চেষ্টা করুন।" });
    } else if (status === 403) {
      res.status(403).json({ error: "API key-টির এই model বা endpoint ব্যবহারের অনুমতি নেই।" });
    } else if (code === "model_not_found") {
      res.status(404).json({ error: "Model পাওয়া যায়নি। API Manager-এ সঠিক model নাম দিন।" });
    } else if (apiMessage) {
      res.status(status).json({ error: apiMessage });
    } else {
      res.status(500).json({ error: "Explanation তৈরি করতে সমস্যা হয়েছে। API connection চেক করুন।" });
    }
  }
});

